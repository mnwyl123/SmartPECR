COMMENT ON COLUMN E_CREDIT_LIM.CREDIT_LIM_TYPE IS '授信额度类型';

COMMENT ON COLUMN E_CREDIT_LIM.LIM_LOOP_FLG IS '额度循环标志';

COMMENT ON COLUMN E_CREDIT_LIM.CREDIT_LIM IS '授信额度';

COMMENT ON COLUMN E_CREDIT_LIM.CY IS '币种';

COMMENT ON COLUMN E_CREDIT_LIM.CON_EFF_DATE IS '额度生效日期';

COMMENT ON COLUMN E_CREDIT_LIM.CON_EXP_DATE IS '额度到期日期';

COMMENT ON COLUMN E_CREDIT_LIM.CON_STATUS IS ' 额度状态';

COMMENT ON COLUMN E_CREDIT_LIM.CREDIT_REST IS '授信限额';

COMMENT ON COLUMN E_CREDIT_LIM.CREDIT_REST_CODE IS '授信限额编号';

COMMENT ON COLUMN E_CREDIT_LIM.EGA_CTRCT_BS_SGMT_ID IS '数据外键';

COMMENT ON COLUMN E_CREDIT_LIM.RPT_DATE IS '报送日期';

COMMENT ON COLUMN E_CREDIT_LIM.EXTEND1 IS '预留字段1';

COMMENT ON COLUMN E_CREDIT_LIM.EXTEND2 IS '预留字段2';

