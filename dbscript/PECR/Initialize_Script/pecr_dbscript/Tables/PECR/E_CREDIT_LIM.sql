DROP TABLE E_CREDIT_LIM CASCADE CONSTRAINTS;

CREATE TABLE E_CREDIT_LIM
(
  EGC_CREDIT_LIM_SGMT_ID  NCHAR(32)             NOT NULL,
  CREDIT_LIM_TYPE         NCHAR(2),
  LIM_LOOP_FLG            NCHAR(1),
  CREDIT_LIM              NUMBER,
  CY                      NCHAR(3),
  CON_EFF_DATE            NVARCHAR2(10),
  CON_EXP_DATE            NVARCHAR2(10),
  CON_STATUS              NCHAR(1),
  CREDIT_REST             NUMBER,
  CREDIT_REST_CODE        NVARCHAR2(60),
  EGA_CTRCT_BS_SGMT_ID    NCHAR(32)             NOT NULL,
  RPT_DATE                NCHAR(10),
  EXTEND1                 NVARCHAR2(100),
  EXTEND2                 NVARCHAR2(100),
  EXTEND3                 NVARCHAR2(100)
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

