DROP TABLE FEED_BACK_REPORT_ERR_MESSAGE CASCADE CONSTRAINTS;

CREATE TABLE FEED_BACK_REPORT_ERR_MESSAGE
(
  FEED_BACK_REPORT_ERROR_MESS_ID  NCHAR(32)     NOT NULL,
  FEED_BACK_REPORT_CODE_ID        NVARCHAR2(50),
  ERROR_MESSAGE                   NVARCHAR2(2000),
  RECORD_ID                       NCHAR(32),
  REPORT_MESSAGE_ID               NVARCHAR2(60),
  REPORT_MESSAGE_TYPE             NVARCHAR2(10),
  CREATE_TIME                     NVARCHAR2(10),
  REPORT_MESSAGE_NAME             NVARCHAR2(100),
  FEED_BACK_REPORT_NAME           NVARCHAR2(255),
  FB_CODE                         NVARCHAR2(255),
  FB_TAG                          NVARCHAR2(255)
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

