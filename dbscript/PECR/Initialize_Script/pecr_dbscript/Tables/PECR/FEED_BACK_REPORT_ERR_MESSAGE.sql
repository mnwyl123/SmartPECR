COMMENT ON COLUMN FEED_BACK_REPORT_ERR_MESSAGE.RECORD_ID IS '记录ID';

COMMENT ON COLUMN FEED_BACK_REPORT_ERR_MESSAGE.REPORT_MESSAGE_ID IS '报文文件ID';

COMMENT ON COLUMN FEED_BACK_REPORT_ERR_MESSAGE.REPORT_MESSAGE_TYPE IS '记录类型';

COMMENT ON COLUMN FEED_BACK_REPORT_ERR_MESSAGE.CREATE_TIME IS '创建时间';

COMMENT ON COLUMN FEED_BACK_REPORT_ERR_MESSAGE.REPORT_MESSAGE_NAME IS '报文名称';

COMMENT ON COLUMN FEED_BACK_REPORT_ERR_MESSAGE.FEED_BACK_REPORT_NAME IS '反馈报文名称';

