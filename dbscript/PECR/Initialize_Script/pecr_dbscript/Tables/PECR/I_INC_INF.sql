DROP TABLE I_INC_INF CASCADE CONSTRAINTS;

CREATE TABLE I_INC_INF
(
  PAI_INC_INF_SGMT_ID  NCHAR(32)                NOT NULL,
  ANNL_INC             NUMBER,
  TAX_INCOME           NUMBER,
  INC_INFO_UP_DATE     NCHAR(10),
  PAA_BS_SGMT_ID       NCHAR(32)                NOT NULL,
  RPT_DATE             NCHAR(10),
  EXTEND1              NVARCHAR2(100),
  EXTEND2              NVARCHAR2(100),
  EXTEND3              NVARCHAR2(100)
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

COMMENT ON TABLE I_INC_INF IS '110-个人基本信息记录收入信息段';

COMMENT ON COLUMN I_INC_INF.PAI_INC_INF_SGMT_ID IS '主键';

COMMENT ON COLUMN I_INC_INF.ANNL_INC IS '自报年收入';

COMMENT ON COLUMN I_INC_INF.TAX_INCOME IS '纳税年收入';

COMMENT ON COLUMN I_INC_INF.INC_INFO_UP_DATE IS '信息更新日期';

COMMENT ON COLUMN I_INC_INF.PAA_BS_SGMT_ID IS '数据外键';

COMMENT ON COLUMN I_INC_INF.RPT_DATE IS '报送日期';

COMMENT ON COLUMN I_INC_INF.EXTEND1 IS '预留字段1';

COMMENT ON COLUMN I_INC_INF.EXTEND2 IS '预留字段2';

COMMENT ON COLUMN I_INC_INF.EXTEND3 IS '预留字段3';
