COMMENT ON COLUMN E_ACCT_CRED.EDA_ACCT_BS_SGMT_ID IS '数据外键';

COMMENT ON COLUMN E_ACCT_CRED.RPT_DATE IS '报送日期';

COMMENT ON COLUMN E_ACCT_CRED.EXTEND1 IS '预留字段1';

COMMENT ON COLUMN E_ACCT_CRED.EXTEND2 IS '预留字段2';

COMMENT ON COLUMN E_ACCT_CRED.EXTEND3 IS '预留字段3';
