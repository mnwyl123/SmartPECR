DROP TABLE E_ACCT_CRED CASCADE CONSTRAINTS;

CREATE TABLE E_ACCT_CRED
(
  EDE_ACCT_CRED_SGMT_ID  NCHAR(32)              NOT NULL,
  MCC                    NVARCHAR2(60),
  EDA_ACCT_BS_SGMT_ID    NCHAR(32)              NOT NULL,
  RPT_DATE               NCHAR(10),
  EXTEND1                NVARCHAR2(100),
  EXTEND2                NVARCHAR2(100),
  EXTEND3                NVARCHAR2(100)
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

