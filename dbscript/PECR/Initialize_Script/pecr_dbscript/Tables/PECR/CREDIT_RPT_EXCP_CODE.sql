DROP TABLE CREDIT_RPT_EXCP_CODE CASCADE CONSTRAINTS;

CREATE TABLE CREDIT_RPT_EXCP_CODE
(
  CREDIT_RPT_EXCP_CODE  NVARCHAR2(32)           NOT NULL,
  EXCP_CODE             NVARCHAR2(32)           NOT NULL,
  EXCP_NUM              NVARCHAR2(10)           NOT NULL,
  EXCP_INFO             NVARCHAR2(2000)         NOT NULL,
  CREATE_TIME           NVARCHAR2(10)           NOT NULL,
  CREAT_USER            NVARCHAR2(20)           NOT NULL
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

