COMMENT ON COLUMN CREDIT_RPT_EXCP_CODE.CREDIT_RPT_EXCP_CODE IS '征信报送异常日志主键';

COMMENT ON COLUMN CREDIT_RPT_EXCP_CODE.EXCP_CODE IS '异常代码';

COMMENT ON COLUMN CREDIT_RPT_EXCP_CODE.EXCP_NUM IS '异常标号';

COMMENT ON COLUMN CREDIT_RPT_EXCP_CODE.EXCP_INFO IS '异常信息';

COMMENT ON COLUMN CREDIT_RPT_EXCP_CODE.CREATE_TIME IS '日志创建时间';

COMMENT ON COLUMN CREDIT_RPT_EXCP_CODE.CREAT_USER IS '创建人员';
