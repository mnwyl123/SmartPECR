DROP TABLE E_COTA_INF CASCADE CONSTRAINTS;

CREATE TABLE E_COTA_INF
(
  EAH_COTA_INF_SGMT_ID   NCHAR(32)              NOT NULL,
  CON_ADD_DISTRICT_CODE  NCHAR(6),
  CON_ADD                NVARCHAR2(100),
  CON_PHONE              NVARCHAR2(25),
  FIN_CON_PHONE          NVARCHAR2(25),
  COTA_INFO_UP_DATE      NCHAR(10),
  EAA_BS_SGMT_ID         NCHAR(32)              NOT NULL,
  RPT_DATE               NCHAR(10),
  EXTEND1                NVARCHAR2(100),
  EXTEND2                NVARCHAR2(100),
  EXTEND3                NVARCHAR2(100)
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

