DROP TABLE DATA_CHECK_RESULT CASCADE CONSTRAINTS;

CREATE TABLE DATA_CHECK_RESULT
(
  CHECK_TASK_NAME   NVARCHAR2(100)              NOT NULL,
  BELONG_SUBJECT    NVARCHAR2(20)               NOT NULL,
  EXECUTE_RESULT    NVARCHAR2(10)               NOT NULL,
  START_TIME        NVARCHAR2(30)               NOT NULL,
  SPEND_TIME        NVARCHAR2(10)               NOT NULL,
  RESULT_STATISTIC  NVARCHAR2(2000)             NOT NULL
)
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

COMMENT ON TABLE DATA_CHECK_RESULT IS '数据校验结果表';

COMMENT ON COLUMN DATA_CHECK_RESULT.CHECK_TASK_NAME IS '校验任务名称';

COMMENT ON COLUMN DATA_CHECK_RESULT.BELONG_SUBJECT IS '所属主题';

COMMENT ON COLUMN DATA_CHECK_RESULT.EXECUTE_RESULT IS '执行结果';

COMMENT ON COLUMN DATA_CHECK_RESULT.START_TIME IS '开始时间';

COMMENT ON COLUMN DATA_CHECK_RESULT.SPEND_TIME IS '耗时';

COMMENT ON COLUMN DATA_CHECK_RESULT.RESULT_STATISTIC IS '执行结果统计';
